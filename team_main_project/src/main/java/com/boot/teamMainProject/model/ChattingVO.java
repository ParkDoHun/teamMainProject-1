package com.boot.teamMainProject.model;

public class ChattingVO {
	private int gatNo;
	private String chatWelcome;
	private String chatInfo;
	private String chatTime;
	private String chatArea;
	
	public int getGatNo() {
		return gatNo;
	}
	public void setGatNo(int gatNo) {
		this.gatNo = gatNo;
	}
	public String getChatWelcome() {
		return chatWelcome;
	}
	public void setChatWelcome(String chatWelcome) {
		this.chatWelcome = chatWelcome;
	}
	public String getChatInfo() {
		return chatInfo;
	}
	public void setChatInfo(String chatInfo) {
		this.chatInfo = chatInfo;
	}
	public String getChatTime() {
		return chatTime;
	}
	public void setChatTime(String chatTime) {
		this.chatTime = chatTime;
	}
	public String getChatArea() {
		return chatArea;
	}
	public void setChatArea(String chatArea) {
		this.chatArea = chatArea;
	}
	
	
}
